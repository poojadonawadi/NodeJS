// Regular function
let demo1 = function(){
    console.log("Hii")
}
demo1();

//Arrow function
let demo2 = () =>{
    console.log("hii");
}
demo2()

//Arrow function with one argument
let demo3 = (a) => {
    console.log(a)
}
demo3(10);

//Arrow function with two arguments
let product = (x, y) => {
    console.log(x*y);
}
product(3 , 4);